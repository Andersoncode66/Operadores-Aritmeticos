public class OperadoresAritmeticosMultiplicação {
    public static void main(String[] args) {
    // Exemplo de operadores aritméticos em Java: adição, subtração e multiplicação

    // Declarando e inicializando variáveis
    int numero1 = 8;
    int numero2 = 5;

    // Realizando a adição
    int resultadoAdicao = numero1 + numero2;

    // Realizando a subtração
    int resultadoSubtracao = numero1 - numero2;

    // Realizando a multiplicação
    int resultadoMultiplicação = numero1 * numero2;

    // Exibindo os resultados
    System.out.println("Primeiro número: " + numero1);
    System.out.println("Segundo número: " + numero2);
    System.out.println("Resultado da adição: " + resultadoAdicao);
    System.out.println("Resultado da subtração: " + resultadoSubtracao);
    System.out.println("Resultado da multiplicação: " + resultadoMultiplicação);

    }
}
