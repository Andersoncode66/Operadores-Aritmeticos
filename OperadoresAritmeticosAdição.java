public class OperadoresAritmeticosAdição {
    public static void main(String[] args) {
        // Exemplo de operador de adição em Java

        // Declarando  e inicializando variáveis
        int numero1 = 25;
        int numero2= 17;

        // Realizando a adição
        int resultadoAdicao = numero1 + numero2;
        

        // Exibindo o resultado
        System.err.println("Primeiro número:" + numero1);
        System.out.println("Segundo número:" + numero2);
        System.out.println("Resultado da adição:" + resultadoAdicao);
    }
}